// detectLocale: saved language > browser language(s) > English.
function detectLocale() {
    try {
        const saved = localStorage.getItem('lang');
        if (saved && MESSAGES[saved]) return saved;
    } catch (e) {}
    const cands = (navigator.languages && navigator.languages.length) ? navigator.languages : [navigator.language || 'en'];
    for (const c of cands) {
        const code = String(c).toLowerCase().split('-')[0];
        if (MESSAGES[code]) return code;
    }
    return 'en';
}

